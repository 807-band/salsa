CREATE TABLE Evaluation (
  evalID INT PRIMARY KEY AUTO_INCREMENT,
  userID INT NOT NULL,
  evaluatorID INT NOT NULL,
  station INT NOT NULL,
  passed BOOLEAN,
  evalTime DATETIME DEFAULT NOW(),

  FOREIGN KEY (userID) REFERENCES Users (userID) ON DELETE CASCADE,
  FOREIGN KEY (evaluatorID) REFERENCES Users (userID) ON DELETE CASCADE,
  FOREIGN KEY (station) REFERENCES Station (sID) ON DELETE CASCADE
);

CREATE TABLE EvaluationItems (
  evalID INT NOT NULL,
  itemID INT NOT NULL,
  status BOOLEAN,

  FOREIGN KEY (evalID) REFERENCES Evaluation (evalID) ON DELETE CASCADE,
  FOREIGN KEY (itemID) REFERENCES StationItem (itemID) ON DELETE CASCADE
);
